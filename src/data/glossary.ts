/** Словоформы būt в контексте предложения */
export function glossButFuture(sentence: string): string | null {
  if (/\bbūs\b/i.test(sentence)) {
    if (/\bes\b.*\bbūs\b/i.test(sentence) || /\bbūs\b.*\bes\b/i.test(sentence)) return 'я буду'
    if (/\btu\b.*\bbūs\b/i.test(sentence) || /\bbūs\b.*\btu\b/i.test(sentence)) return 'ты будешь'
    if (/\bviņš\b|\bviņa\b|\btas\b|\bšis\b/i.test(sentence)) return 'будет'
    if (/\bmēs\b/i.test(sentence)) return 'мы будем'
    if (/\bjūs\b/i.test(sentence)) return 'вы будете'
    return 'будет (будущ. от «būt»)'
  }
  return null
}

export function glossTe(sentence: string): string | null {
  if (/\bte\b/i.test(sentence)) {
    if (/\bte būs\b/i.test(sentence)) return 'здесь (место)'
    if (/\bte ir\b/i.test(sentence)) return 'здесь (место)'
    return 'здесь'
  }
  return null
}
